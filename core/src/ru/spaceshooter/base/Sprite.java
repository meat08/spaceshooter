package ru.spaceshooter.base;

import com.badlogic.gdx.Gdx;
import com.badlogic.gdx.graphics.g2d.SpriteBatch;
import com.badlogic.gdx.graphics.g2d.TextureRegion;
import com.badlogic.gdx.math.Vector2;

import ru.spaceshooter.math.Rect;
import ru.spaceshooter.utils.Regions;

public class Sprite extends Rect {

    protected static final float ANIMATE_INTERVAL = 0.018f;

    protected float angle;
    protected float scale = 1f;
    protected TextureRegion[] regions;
    protected int frame = 0;
    protected boolean destroyed;
    protected boolean isAnimateEnd;
    protected float baseAnimateInterval;

    private float animateTimer;

    public Sprite() {
    }

    public Sprite(TextureRegion region) {
        regions = new TextureRegion[1];
        regions[0] = region;
    }

    public Sprite(TextureRegion region, int rows, int cols, int frames) {
        regions = Regions.split(region, rows, cols, frames);
        this.baseAnimateInterval = ANIMATE_INTERVAL;
    }

    public void setHeightProportion(float height) {
        setHeight(height);
        float aspect = regions[frame].getRegionWidth() / (float) regions[frame].getRegionHeight();
        setWidth(height * aspect);
    }

    public void update(float delta) {
        animateTimer += delta;
        isAnimateEnd = false;
        if (animateTimer >= baseAnimateInterval) {
            animateTimer = 0f;
            if (++frame == regions.length) {
                isAnimateEnd = true;
            }
        }
    }

    public void draw(SpriteBatch batch) {
        try {
            batch.draw(
                    regions[frame],
                    getLeft(), getBottom(),
                    halfWidth, halfHeight,
                    getWidth(), getHeight(),
                    scale, scale,
                    angle
            );
        } catch (Exception e) {
            Gdx.app.log("FATAL", "Draw error", e);
        }
    }

    public void resize(Rect worldBounds) {
    }

    public boolean touchDown(Vector2 touch, int pointer, int button) {
        return false;
    }

    public boolean touchUp(Vector2 touch, int pointer, int button) {
        return false;
    }

    public boolean keyDown(int keycode) {
        return false;
    }

    public boolean keyUp(int keycode) {
        return false;
    }

    public boolean touchDragged(Vector2 touch, int pointer) {
        return false;
    }

    public float getAngle() {
        return angle;
    }

    public void setAngle(float angle) {
        this.angle = angle;
    }

    public int getFrame() {
        return frame;
    }

    public void setFrame(int frame) {
        this.frame = frame;
    }

    public float getScale() {
        return scale;
    }

    public void setScale(float scale) {
        this.scale = scale;
    }

    public void destroy() {
        destroyed = true;
    }

    public void flushDestroy() {
        destroyed = false;
    }

    public boolean isDestroyed() {
        return destroyed;
    }
}
